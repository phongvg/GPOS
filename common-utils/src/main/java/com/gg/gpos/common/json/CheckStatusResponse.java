public class CheckStatusResponse {
	private String code;
	private String mess;
	
	private String data;
	
	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}

	public String getCode() {
		return code;
	}
	
	public void setCode(String code) {
		this.code = code;
	}
	
	public String getMess() {
		return mess;
	}
	
	public void setMess(String mess) {
		this.mess = mess;
	}
}
